#include<iostream>
using namespace std;
int i, j, n, k[50];
int Max(int k[], int n)
{
	int aSum, bSum = 0;
	for (int i = 0; i < n; i++)
	{
		aSum = 0;
		for (int j = i; j < n; j++)
		{
			aSum += k[j];
			if (aSum > bSum)
				bSum = aSum;
		}
	}
	return bSum;
}
int main()
{
	int n;
	cin >> n;
	for (int i = 0; i < n; i++)
		cin >> k[i];
	if (n <= 0)
		cout << 0;
	else
		cout << Max(k, n);
	return 0;
}
